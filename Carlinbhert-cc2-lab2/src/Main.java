public class Main {
    public static void main(String[] args) {

//        Length and Width values
        int length = 20;
        int width = 15;

//        Displaying Length/Width of the garden
        System.out.println("Length: " + length + " meters");
        System.out.println("Width: " + width + " meters");

//        Calculating the Area of the garden
        System.out.println("Area: " + (length * width) + " meters squared");
//        Calculating the Perimeter of the garden
        System.out.println("Perimeter: " + (2 * (length + width)) + " meters");
    }
}
